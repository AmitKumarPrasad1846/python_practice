# File I/O in Python

# Python can be used to perform operations on a file (read & write data)

# Types of all files:

# 1. Text Files: .txt, .docx, .log, .csv, .html, .json, .py, etc.
#    - Store data in human-readable format
#    - Can be opened in any text editor
#    - Contains characters, letters, numbers, punctuation

# 2. Binary Files: .mp4, .mov, .png, .jpeg, .pdf, .exe, .zip, .mp3, etc.
#    - Store data in binary format (0s and 1s)
#    - Not human-readable
#    - Contains machine code, images, audio, video
