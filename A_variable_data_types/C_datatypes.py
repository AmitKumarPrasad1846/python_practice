# Data Types in Python:-
# - Integers: +ve, -ve, 0 → examples: 25, -25, 0 (int)
# - String: "shradha", "Hello"
# - Float: 3.99, 2.5, 9.0
# - Boolean: True, False
# - None: a = None

name = "hello world"
age = 19
price = 56.55
enter = True
a = None

print(type(name)) # string 
print(type(age)) # integer
print(type(price)) # float
print(type(enter)) # boolean
print(type(a)) # none