# -------------------------------
# Types of Operators in Python
# -------------------------------

# An operator is a symbol that performs an operation between operands.

# 1. Arithmetic Operators
# +, -, *, /, %, **
a = 10 + 5   # Addition
b = 10 - 5   # Subtraction
c = 10 * 5   # Multiplication
d = 10 / 5   # Division
e = 10 % 3   # Modulus (remainder)
f = 2 ** 3   # Exponentiation

# 2. Relational / Comparison Operators
# ==, !=, >, <, >=, <=
x = (10 == 5)   # False
y = (10 != 5)   # True
z = (10 > 5)    # True
w = (10 < 5)    # False
p = (10 >= 5)   # True
q = (10 <= 5)   # False

# 3. Assignment Operators
# =, +=, -=, *=, /=, %=, **=
num = 10
num += 5   # num = num + 5 → 15
num -= 3   # num = num - 3 → 12
num *= 2   # num = num * 2 → 24
num /= 4   # num = num / 4 → 6.0
num %= 5   # num = num % 5 → 1.0
num **= 3  # num = num ** 3 → 1.0

# 4. Logical Operators - works on boolean values
# not, and, or
a = True
b = False
print(not a)       # False
print(a and b)     # False
print(a or b)      # True

